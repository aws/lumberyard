#include "LyShine_precompiled.h"
#include "UiRenderer.h"

////////////////////////////////////////////////////////////////////////////////////////////////////
// PUBLIC MEMBER FUNCTIONS
////////////////////////////////////////////////////////////////////////////////////////////////////

////////////////////////////////////////////////////////////////////////////////////////////////////
UiRenderer::UiRenderer()
    : m_baseState(GS_DEPTHFUNC_LEQUAL)
    , m_stencilRef(0)
    , m_isRenderingToMask(false)
{
}

////////////////////////////////////////////////////////////////////////////////////////////////////
UiRenderer::~UiRenderer()
{
}

////////////////////////////////////////////////////////////////////////////////////////////////////
void UiRenderer::BeginCanvasRender(AZ::Vector2 viewportSize)
{
    // eventually this will setup 2D draw mode
    m_baseState = GS_NODEPTHTEST;
    m_stencilRef = 0;

    // Begin 2D drawing mode
    gEnv->pLyShine->GetDraw2d()->BeginDraw2d(viewportSize, false);
}

////////////////////////////////////////////////////////////////////////////////////////////////////
void UiRenderer::EndCanvasRender()
{
    // End 2D drawing mode
    gEnv->pLyShine->GetDraw2d()->EndDraw2d();
}

////////////////////////////////////////////////////////////////////////////////////////////////////
int UiRenderer::GetBaseState()
{
    return m_baseState;
}

////////////////////////////////////////////////////////////////////////////////////////////////////
void UiRenderer::SetBaseState(int state)
{
    m_baseState = state;
}

////////////////////////////////////////////////////////////////////////////////////////////////////
uint32 UiRenderer::GetStencilRef()
{
    return m_stencilRef;
}

////////////////////////////////////////////////////////////////////////////////////////////////////
void UiRenderer::SetStencilRef(uint32 stencilRef)
{
    m_stencilRef = stencilRef;
}

////////////////////////////////////////////////////////////////////////////////////////////////////
void UiRenderer::IncrementStencilRef()
{
    ++m_stencilRef;
}

////////////////////////////////////////////////////////////////////////////////////////////////////
void UiRenderer::DecrementStencilRef()
{
    --m_stencilRef;
}

////////////////////////////////////////////////////////////////////////////////////////////////////
bool UiRenderer::IsRenderingToMask()
{
    return m_isRenderingToMask;
}

////////////////////////////////////////////////////////////////////////////////////////////////////
void UiRenderer::SetIsRenderingToMask(bool isRenderingToMask)
{
    m_isRenderingToMask = isRenderingToMask;
}

////////////////////////////////////////////////////////////////////////////////////////////////////
void UiRenderer::PushAlphaFade(float alphaFadeValue)
{
    float currentAlphaFade = GetAlphaFade();
    m_alphaFadeStack.push(alphaFadeValue * currentAlphaFade);
}

////////////////////////////////////////////////////////////////////////////////////////////////////
void UiRenderer::PopAlphaFade()
{
    m_alphaFadeStack.pop();
}

////////////////////////////////////////////////////////////////////////////////////////////////////
float UiRenderer::GetAlphaFade() const
{
    float alphaFade = 1.0f; // by default nothing is faded

    if (!m_alphaFadeStack.empty())
    {
        alphaFade = m_alphaFadeStack.top();
    }
    return alphaFade;
}

